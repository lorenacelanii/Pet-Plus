public class Pet {

    
    private String nomePet;
    private String idadePet;
    private int idCliente;
    private String racaPet;
    private int idPet;

    /**
     * @return the nomePet
     */
    public String getNomePet() {
        return nomePet;
    }

    /**
     * @param nomePet the nomePet to set
     */
    public void setNomePet(String nomePet) {
        this.nomePet = nomePet;
    }

    /**
     * @return the idadePet
     */
    public String getIdadePet() {
        return idadePet;
    }

    /**
     * @param idadePet the idadePet to set
     */
    public void setIdadePet(String idadePet) {
        this.idadePet = idadePet;
    }

    

    /**
     * @return the racaPet
     */
    public String getRacaPet() {
        return racaPet;
    }

    /**
     * @param racaPet the racaPet to set
     */
    public void setRacaPet(String racaPet) {
        this.racaPet = racaPet;
    }
    
    /**
     * @return the idPet
     */
    public int getIdPet() {
        return idPet;
    }

    /**
     * @param idPet the idPet to set
     */
    public void setIdPet(int idPet) {
        this.idPet = idPet;
    }

    /**
     * @return the idCliente
     */
    public int getIdCliente() {
        return idCliente;
    }

    /**
     * @param idCliente the idCliente to set
     */
    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }
   
}